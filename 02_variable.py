# Integer variable
age = 22

# Float variable
cgpa = 3.61

# String variable
name = "Rifat"

# Boolean variable
is_student = True

# List variable (stores multiple values)
skills = ["Python", "HTML", "CSS"]

# Tuple variable (immutable collection)
marks = (80, 85, 90)

# Dictionary variable (key-value pairs)
student = {
    "name": "Rifat",
    "age": 22
}

# Display all variables
print("Name:", name)
print("Age:", age)
print("CGPA:", cgpa)
print("Student Status:", is_student)
print("Skills:", skills)
print("Marks:", marks)
print("Student Information:", student)